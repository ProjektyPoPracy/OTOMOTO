

checkCorrectnessOfOfferValues <- 
  function(
    offerId,
    offerPrice,
    dealerType,
    dealerLocation,
    paramLabels,
    paramValues,
    equipmentLabels,
    description
  )
  {
    checkCorrectnessOfOfferId(offerId = offerId)
    checkCorrectnessOfOfferPrice(offerPrice = offerPrice)
    checkCorrectnessOfDealerType(dealerType = dealerType)
    checkCorrectnessOfDealerLocation(dealerLocation = dealerLocation)
    checkCorrectnessOfParamLabelsAndValues(labels = paramLabels, values = paramValues)
    checkCorrectnessOfEquipmentLabels(equipment = equipmentLabels)
    checkCorrectnessOfDescription(description = description)
    
    return(invisible())
  }
